package com.hello.spring.aop.tradition;

/**
 * 2016-10-21.
 */
public class ArithmeticAopImpl implements Arithmetic {

    public int add(int i, int j) {
        int result = i + j;
        return result;
    }

    public int sub(int i, int j) {
        int result = i - j;
        return result;
    }

    public int mul(int i, int j) {
        int result = i * j;
        return result;
    }

    public int div(int i, int j) {
        int result = i / j;
        return result;
    }
}
